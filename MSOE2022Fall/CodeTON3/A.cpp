void solve() {
    int n;
    cin >> n;
    int m;
    cin >> m;
    bool pass = true;
    for (int i = 1; i < n; ++i) {
        int v;
        cin >> v;
        if (v < m) {
            pass = false;
        }
    }
    cout << (pass ? "Yes" : "No") << endl;
}

